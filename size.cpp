#include<iostream>
using namespace std;

int main(){
      int a=12;
      cout<<"Size of int "<<sizeof(a)<<endl;

      float b=12.5;
      cout<<"Size of float "<<sizeof(b)<<endl;

      char c;
      cout<<"Size of char "<<sizeof(c)<<endl;

      bool d;
      cout<<"Size of boolean "<<sizeof(d)<<" byte"<<endl;

}
